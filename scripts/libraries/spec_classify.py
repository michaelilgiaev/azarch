# Precompile the DESC keyword rules as WORD-BOUNDARY regexes. Raw substring
# matching produced false positives -- "ssl" in "lossless", "disk" in "diskless",
# "crypto" in "non-cryptographic" -- so every keyword must match as a whole word
# (or hyphenated word). Multi-word keys (e.g. "virtual machine") are matched with
# a flexible separator.
def _compile_desc_rules(rules):
    compiled = []
    for kw, cat in rules:
        parts = [re.escape(w) for w in kw.split()]
        pat = r"\b" + r"[\s-]+".join(parts) + r"\b"
        compiled.append((re.compile(pat), cat))
    return compiled


_DESC_RULES_RX = _compile_desc_rules(DESC_RULES)


def _provides_shared_lib(rec):
    for prov in rec.get("provides_raw", ()):
        base = prov.split("=", 1)[0]
        if ".so" in base:
            return True
    return False


def category_of(pkg, packages):
    """Return the single best human-language category for a package."""
    rec = packages.get(pkg, {})

    # 1. curated exact name
    if pkg in CURATED:
        return CURATED[pkg]

    # 2. Arch group
    for g in rec.get("groups", ()):
        if g in GROUP_ROLE:
            return GROUP_ROLE[g]

    # 2b. exact build-toolchain names
    if pkg in DEV_TOOL_NAMES:
        return "Developer tools"

    # 3. name patterns
    for pre, cat in NAME_PREFIX_RULES:
        if pkg.startswith(pre):
            return cat
    for sub, cat in NAME_SUBSTR_RULES:
        if sub in pkg:
            return cat

    # 4. description keywords (whole-word match; see _compile_desc_rules)
    desc = (rec.get("desc") or "").lower()
    for pat, cat in _DESC_RULES_RX:
        if pat.search(desc):
            return cat

    # 5. fallback: shared library vs generic system component
    if pkg.startswith("lib") or _provides_shared_lib(rec):
        return "Shared library"
    return "System"


